package streams;

public class Vocacao {
	
	final String nome;
	final int level;
	final boolean poi;
	final boolean promotion;
	
	
	public Vocacao(String nome, int level, boolean poi, boolean promotion) {
		this.nome = nome;
		this.level = level;
		this.poi = poi;
		this.promotion = promotion;
	}
	
	

}
